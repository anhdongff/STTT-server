-- Recreate schema sttt
DROP SCHEMA IF EXISTS sttt CASCADE;
CREATE SCHEMA sttt;

SET search_path TO sttt;

-- =========================
-- ENUMs
-- =========================

DROP TYPE IF EXISTS job_status CASCADE;
CREATE TYPE job_status AS ENUM ('pending', 'running', 'completed', 'error');

DROP TYPE IF EXISTS job_type CASCADE;
CREATE TYPE job_type AS ENUM ('transcribe', 'translate');

DROP TYPE IF EXISTS output_type CASCADE;
CREATE TYPE output_type AS ENUM ('text', 'srt');

-- =========================
-- 2) users
-- =========================
CREATE TABLE sttt.users (
  id              BIGSERIAL PRIMARY KEY,
  email           TEXT UNIQUE,
  display_name    TEXT,
  password_hash   TEXT NOT NULL, -- store bcrypt hash (not plaintext)

  created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- updated_at auto-update
CREATE OR REPLACE FUNCTION sttt.set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_users_updated_at ON sttt.users;
CREATE TRIGGER trg_users_updated_at
BEFORE UPDATE ON sttt.users
FOR EACH ROW
EXECUTE FUNCTION sttt.set_updated_at();

